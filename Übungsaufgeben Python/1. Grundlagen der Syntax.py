#Grundlagen der Syntax

#1.  "Hallo Welt" ausgeben: Schreibe ein Programm, das Hallo Welt! auf der Konsole ausgibt.
#2.  Zwei Zahlen addieren: Lasse den Benutzer zwei Zahlen eingeben und gebe ihre Summe aus.
#3.  Variablen tauschen: Tausche den Wert zweier Variablen, z. B. a = 5 und b = 10, sodass am Ende a = 10 und b = 5 ist.